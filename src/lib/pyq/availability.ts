// PYQ — Exam-Type Availability Map (lightweight)
//
// This module lists, per chapter slug, whether at least one question
// exists for each exam target (NEET = an untagged question; JEE Main /
// JEE Advanced = a question with the matching `examType`). It contains
// NO question content, so client components that merely need to know
// "does this chapter have PYQ for exam X?" can import this instead of
// the full registry — keeping heavy question/option/solution text out
// of the client bundle.
//
// KEEP IN SYNC with index.ts. A build-time guard (see index.ts bottom)
// asserts the slug set matches, so a forgotten chapter fails the build,
// not prod. The per-exam-type booleans themselves are derived once from
// the real registry and must be regenerated by hand if a chapter's PYQ
// exam-type mix changes.
// ─────────────────────────────────────────────────────────────────

